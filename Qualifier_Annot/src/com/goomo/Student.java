package com.goomo;

public class Student {
	private Integer age;
	private Student name;

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public Student getName() {
		return name;
	}

	public void setName(Student name) {
		this.name = name;
	}

}
